namespace Immediate.Handlers.Shared;

/// <summary>
///		Specifies which type of handler should be rendered
/// </summary>
public enum RenderMode
{
	/// <summary>
	///		Represents an invalid entry, and should not be used.
	/// </summary>
	None,

	/// <summary>
	///		A common handler should be rendered.
	/// </summary>
	Normal,
}
